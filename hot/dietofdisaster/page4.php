            <h1><strong>Overuse of Fresh Water</strong><strong></strong></h1>
            <p>&nbsp;Fresh water is in short supply! Only 2.5 per cent of all water on the planet is fresh and 70 per cent of this is locked up in glaciers and permanent snow. What was once a slow process of summer melt and winter renewal fed rivers and aquifers the world over. With global warming, glaciers are disappearing at an unprecedented and accelerating rate and the valuable fresh water is simply running into the sea. </p>
            <p> Currently, more than 2.3 billion people in 21 countries live in areas which are classified as ‘water stressed’ while a further 1.7 billion live in areas of scarcity. More than one billion people have little access to clean water. In is in these areas where most agricultural expansion and population growth are taking place.</p>
            <p> As a direct consequence of the increase in water demand – the majority for agriculture and the lion’s share of this for animal agriculture - 64 per cent of the world’s population will live in ‘water stressed’ areas by 2025. An International Water Management Institute assessment in 2000 estimated that one-third of the world’s population will live in areas of ‘absolute water scarcity’. These include Pakistan, South Africa and large parts of China and India (UN FAO, 2006). <br>
              Agriculture is the biggest user of fresh water, demanding 70 per cent of all that is available while in many third world countries the figure is as high as 85-95 per cent. Agriculture also accounts for 93 per cent of all depletion – drawing water unsustainably from underground aquifers, for example.<a name="Waterusage"></a> </p>
            <p>            And the UN FAO says, decision makers often do not understand the demand that livestock make on water both directly and indirectly and as a consequence they simply ignore it. In fact, livestock’s use of water is high and growing.</p>
            <p> There are various figures available to show how much more water is required to produce meat than plant foods and they vary considerably in detail but the overall evidence is consistent – meat and dairy require hugely more water.<br>
              Water constitutes 60-70 per cent of the body weight of most animals and a cow will drink up to 127 litres a day, a pig 46.7 litres and 100 chickens up to 62 litres. The water used for cleaning, processing and slaughtering chickens alone can amount to 15 litres per kilo – more than 60 litres per bird (UN FAO, 2006).</p>
            <p> It takes 1000 litres of water to produce one kilogram of wheat, yet it takes 100,000 litres of water to produce one kilogram of beef (Godrej D, 2001). The University of California studied water use in their state, where most agricultural land is irrigated, and said it uses between 20 to 30 gallons of water to produce vegetables such as tomatoes, potatoes and carrots to create one edible pound of food. It takes 441 gallons of water to make a pound of beef (Beckett JL, 1993).</p>
            <p> The disappearance of fresh water in many regions poses a real threat to the stability of the world. Numerous countries are in dispute over water supplies and the seeds of future wars are clearly beginning to germinate.<a name="Topsoil"></a><br>
              Just as has happened with the supply of available land to produce food, those with &nbsp; commercial interests and capital will demand the majority whilst the poor and impoverished will go without. This is an appalling judgment on the values of our so-called civilized society.</p>
