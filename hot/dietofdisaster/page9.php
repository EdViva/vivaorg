            <h2><strong>Heavy Metal Pollution</strong></h2>
            <p>Heavy metals are fed to livestock at low concentrations to counter health problems or to act as growth promoters. They include copper, zinc, selenium, cobalt, arsenic, iron and manganese. Animals can absorb only 5 to 15 per cent of the metals they ingest and the remainder is excreted into the environment (UN FAO).</p>
            <h2><strong>Pesticide Pollution</strong></h2>
            <p>It is the enormous demand for fodder which spurs most pesticide use. Without intensive agriculture and the lavish application of both fertilisers and pesticides, including fungicides and herbicides, crop yields would be insufficient to provide the volume of fodder required. Ironically, this very process of dousing the land with chemicals is destroying its fertility and contributing to land degeneration, effectively reducing the amount of land available. It is a self-defeating policy that appears to concern few in government or agriculture.&nbsp; <br>
              Pesticides are central to modern intensive agriculture and their pervasive nature was first highlighted by DDT which was found to have spread to almost every area of the Earth. The list of substances currently known to contain a potential threat to human health has grown dramatically. Residues are found in virtually every piece of meat eaten (Smulders, 1991).</p>
            <p>While usage has decreased slightly in the EU, several hundred different pesticides are currently used in agriculture around the world. The two most important are organochlorine and organophosphorous compounds. They can contaminate soil, water and air, affect non-target creatures and damage the proper functioning of ecosystems.</p>
            <p>There is a wonderful little word called biomagnification which describes how pesticide residues concentrate the further up the food chain you go, resulting in huge concentrations in top predators and humans. </p>
            <p>Pesticides can impact on the health of all animals and plants and can cause cancers, tumours and lesions, disrupt the immune and endocrine systems, affect reproduction and result in birth defects. In practice, the whole food chain can be affected (UN FAO, 2006). Agent orange, the defoliant that has devastated Vietnam, producing monstrous diseases and birth defects, was a dioxin-based herbicide.</p>
            <p>A recent study based in Germany found that areas near to farms using pesticides show up to six times less biodiversity than habitats surrounding organic farmland. Research also shows that reductions in pesticide use can lead directly to the regeneration of failing wildlife populations. </p>
            <p>What amounts to almost unbridled chemical warfare has led to 4.5 billion litres of pesticides being sprayed on to UK land every year. Water companies spend £120 million annually trying to remove pesticides from our water.</p>
