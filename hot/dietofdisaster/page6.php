            <h1><strong>Fish Farming</strong></h1>
            <p>One of the supposed solutions to overfishing is aquaculture, the farming of fish, but sadly it is part of the problem and not part of the cure.</p>
            <p>In 2002, fish farming accounted for 39.8 million tonnes of global fish production (UN FAOb, 2004). As wild stocks decline, fish farming fills the gap and is the fastest growing sector of the world food economy (RSPB, 2004)). In the UK, it is the second largest livestock sector after broiler chickens with almost all the fish being intensively reared (Lymbery, 2002). Almost every supermarket salmon is factory farmed as increasingly are trout. Cod is now also being farmed (Currie, 2004).</p>
            <p>UK production is dominated by two species – salmon (96 per cent) and fresh-water trout (3.7 per cent) (Huntington, 2004). Scottish lochs are a favourite location, with 340 salmon farms. Production increased from 800 tonnes in 1980 to 145,609 tonnes in 2002. Globally, the annual production of farmed salmon grew 16-fold to over one million tonnes between 1985 and 2000, overtaking the catch of wild salmon.</p>
            <p>According to the World Watch Institute: “No form of aquaculture chews through more of the world’s marine life than does salmon farming” (Ryan, 2003). Salmon and most other farmed fish are fed pellets primarily made up of wild-caught fish such as anchovies, mackerel and capelin.</p>
            <p>For every tonne of farmed salmon produced, three to four tonnes of ‘industrial’ fish are caught. In just 15 years, stocks of South American pilchard crashed by 99 per cent, in order to feed farmed fish (Ryan, 2003).</p>
            <p>Because of the cramped conditions in fish farms, sea lice and disease are rife and so fish farmers have resorted to using pesticides, antibiotics, disinfectants and growth promoters in exactly the same way as livestock farmers. As lice are crustaceans, the pesticides used to combat them also kill lobsters, crabs and shrimps (Glover, 2004).</p>
            <p>Fish farming is not, of course, solely a UK or European problem but is devastating some of the most important habitats in the world. Mangrove forests are extraordinary places which fringe much of the world’s coastline and provide the most productive and vital habitat in all the oceans. Ninety per cent of marine fish rely upon them for spawning and over 2,000 species of fish, crustaceans and plants thrive there yet these extraordinary places are being trashed faster than anyone can count and are being replaced with shrimp farms (New Internationalist, 1992).</p>
            <p>Intensive shrimp farms are productive for just 10 years before being abandoned - usually moving elsewhere and destroying more mangroves and more communities. The land left behind is effectively dead – too saline for aquaculture and for the mangroves to regenerate.</p>
            <p>Mangroves once covered three-quarters of the coastlines of tropical and sub-tropical countries such as Thailand, Malaysia, Ecuador, Panama and others. Today, less than 50 per cent remains and more than half of this remnant is degraded. Nearly one quarter has been destroyed in the last two decades, again primarily for shrimp farms (Worldwatch, 2007).</p>
