          <div id="content"><!-- InstanceBeginEditable name="content" --><h1>HOT! Campaign Overview</h1>
            <div class="captionfloatl" style="width:200px"><img src="/campaigns/hot/images/heathermeaty.jpg" alt="" width="200" height="285"><br>
              Heather Mills, 
              Viva! patron<br>
              “Fortunately, as individuals we don’t have to wait for anyone. You can end your involvement in this vandalism overnight and help to secure our children’s future.&nbsp;All you need do is change your diet – it’s that simple.&nbsp;Change your diet – change the world!”</div>
            <p>Viva! launched its national HOT! campaign to help save the environment 
              at Speakers' Corner in London's Hyde Park on November 19 2007.  Its 
              central theme is that the most effective action individuals can take to 
              reduce global warming and other environmental catastrophes is to change 
              their diet - move away from meat, dairy and other animal products and 
              move towards a plant-based diet. </p>
            <p>Launching the HOT! campaign was Heather Mills, a patron of Viva!, who 
              also features in two leaflets and two billboard ads. One concentrates on 
              meat-eaters' role in causing global warming and carries the headline: 
              &quot;<span class="bluetext">Hey Meaty, you're making me so hot!</span>&quot; Another is aimed at people - and 
              organisations - who call themselves environmentalists but still eat 
              meat, fish and dairy. It has the headline: &quot;<span class="bluetext">You haven't got a leg to 
              stand on!</span>&quot;</p>
            <p>The statistics underlining this campaign are drawn from the United 
              Nations Food &amp; Agriculture Organisation 2006 report, Livestock's Long 
              Shadow, and Viva!'s own report, Diet of Disaster. They establish that 
              livestock are the second biggest cause of greenhouse gases at 18 per 
              cent - compared to 13.5 per cent from all the world's different modes of 
              transport combined. They are the major cause of both species extinctions 
              and the felling of tropical forests - 70 per cent of the cleared Amazon 
              is used for grazing and the other 30 per cent for growing animal fodder, 
              with Europe as the biggest importer.</p>
            <p>&quot;<span class="bluetext">The startling truth is that animals farmed for meat and dairy are now 
              one of the greatest threats to the planet</span>&quot; says Heather Mills. &quot;<span class="bluetext">They are 
                at the heart of almost all the world's environmental catastrophes. Viva! 
                has been saying this for years and now the UN has confirmed it with a 
                report so detailed that it can't be ignored - except by government. It 
                remains silent and continues to pour huge subsidies into meat and dairy 
              farming.</span>&quot;</p>
            <h2><strong><img src="/campaigns/hot/images/cow.jpg" alt="" width="200" height="229" class="floatr">Slaughter</strong></h2>
            <p>55 billion animals were slaughtered worldwide last year. Most were factory farmed. Their demand for food is at the heart of all the world’s biggest environmental problems.</p>
            <p>“<span class="bluetext">Livestock’s contribution to environmental problems is on a massive scale. The impact is so significant that it needs to be addressed with urgency.</span>” United Nations Food  &amp; Agriculture Organisation</p>
            <h2><strong>Extinctions</strong></h2>
            <p>Wild plants and animals are disappearing – up to 1000 times faster than normal because the forests and wild areas where they live have been cleared for animal farming.</p>
            <h2><strong><img src="/campaigns/hot/images/earth.jpg" alt="Global Warming" width="200" height="233" class="floatl">Global Warming</strong></h2>
            <p>18 per cent of greenhouse gases are produced by livestock farming – more than all the world’s planes, boats, cars and trains combined. If we don’t act soon global warming will become unstoppable.</p>
            <h2><strong>Acid Rain</strong></h2>
            <p>About two-thirds of the nitrous oxide and ammonia that cause acid rain comes from manure.</p>
            <h2><strong>Spreading Deserts</strong></h2>
            <p>Dry areas make up one-third of the world’s land and overgrazing is destroying 73 per of their ranges. They are turning to desert.</p>
            <h2><strong><img src="/campaigns/hot/images/fishing.jpg" alt="Overfishing" width="200" height="140" class="floatr">Overfishing</strong></h2>
            <p>Fish stocks have been so devastated that 82 per cent are on the road to extinction.</p>
            <h2><strong>Pollution</strong></h2>
            <p>Livestock are responsible for most pollution – animal waste, hormones, antibiotics, pesticides and chemicals. They are killing coral reefs and diseasing humans.</p>
            <h2><strong><img src="/campaigns/hot/images/dryland.jpg" alt="Forest Destruction" width="200" height="268" class="floatl">Wasted Land</strong></h2>
            <p>Livestock use up to 70 per cent of all agricultural land. Many times more people could be fed from the same land on a diet based on plant foods.</p>
            <h2><strong>Forest Destruction</strong></h2>
            <p>Seventy per cent of all cleared Amazon land is used for grazing cattle and most of the remaining 30 per cent for growing animal feed.</p>
            <h2><strong>Superbugs</strong></h2>
            <p>Antibiotic-resistant bacteria such as E coli and deadly superbugs threaten our children’s
              future and we can largely thank intensive livestock farming.</p>
            <h2><strong>Diet of Disaster</strong></h2>
            <p>That’s the only way to describe eating meat, fish and dairy products and most scientists believe there’s little time left to prevent catastrophe.</p>
            <p>That’s the bad news! The good news is – you don’t have to play a part in this rape of the planet. With one simple decision – today – you can slash your contribution to all these devastating environmental catastrophes. It couldn’t be easier.</p>
            <h2>
              Change your diet – change the world</h2>
