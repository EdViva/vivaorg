          <h1>Recipes</h1>
          <h2><strong>Aubergine Towers &amp; Roast Tomato Sauce</strong></h2>
          <p>            Serves 4<br>
              <br>
            60 minutes, including preparation, cooking and baking time – the sauce can be made whilst aubergines are baking </p>
          <p>4 small aubergines<br>
            2 tbsp olive oil<br>
            1 small onion, chopped<br>
            2 garlic cloves, crushed<br>
            2 tsp grated lemon rind<br>
            1 tsp ground cumin<br>
            ½ tsp ground cinnamon<br>
            50g/2oz sultanas<br>
            50g/2oz cashew nuts, toasted and chopped<br>
            2 tsp tahini (sesame seed paste)<br>
            50g/2oz sun-dried tomatoes in oil, drained and chopped<br>
            2 tbsp fresh coriander, chopped<br>
            Salt and pepper<br>
  <br>
  <strong>To serve:</strong><br>
          </p>
          <ol start="1" type="1">
            <li>Slice off top and bottom of aubergines and stand upright. </li>
            <li>Scoop out flesh leaving skin intact. Chop the flesh.</li>
            <li>Heat oil in a frying pan and fry onion, garlic and spices for 5      minutes. </li>
            <li>Preheat oven to 200ºC/400ºF/Gas Mark 6.</li>
            <li>Add aubergine flesh and lemon rind and continue to cook for a      further 8 minutes or until tender. Stir in remaining ingredients and      season to taste.</li>
            <li>Now make the sauce: in a roasting tin, add ingredients for      sauce and place that in oven (45 minutes). </li>
            <li>Meanwhile, spoon the filling for main recipe into the aubergine      cases and brush with oil.</li>
            <li>Add 1cm water to another roasting tin, add aubergines and bake      for 40 minutes. &nbsp;</li>
          </ol>
          The sauce ingredients and main course ingredients will be ready at roughly the same time – all you need to do is quickly blend the sauce and serve it with the aubergine towers.
