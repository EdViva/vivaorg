          <h1>Recipes</h1>
          <h2><strong>Roasted Red Pepper Sauce </strong></h2>
          <p>            Serves 4</p>
          <p>15 minutes</p>
          <p>1 tsp olive oil<br>
            1 onion, chopped<br>
            2 roasted red peppers from a jar (in brine), drained and roughly chopped<br>
            1 clove garlic, crushed<br>
            Drizzle of date or agave syrup – available from health food shops<br>
            ½ tin of chopped tomatoes<br>
            1 tsp mixed dried herbs<br>
            1 tsp soy sauce<br>
            100ml/3½fl oz vegetable stock<br>
            1 tsp balsamic vinegar<br>
            Pinch smoked paprika or ordinary paprika<br>
            Pinch of chilli powder (optional)</p>
          <ol start="1" type="1">
            <li>Sauté the onion in a little olive oil until softened. </li>
            <li>Add the chopped tomatoes and red peppers, then stir in the syrup,      herbs, soy sauce, paprika and chilli powder (if used) balsamic vinegar and      vegetable stock. </li>
            <li>Simmer over a medium heat for 20-25 minutes. </li>
            <li>Use a hand blender or food processor and process until the      sauce is smooth.</li>
          </ol>
          Thanks to Chava Eichner for the sauce recipe<strong>.</strong>
