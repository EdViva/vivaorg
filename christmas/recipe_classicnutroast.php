          <h1>Recipes</h1>
          <h2><strong>Classic Nut Roast </strong></h2>
          <p>            Serves 6-8</p>
          <p>80 minutes: 20 minutes preparation time, 40-60 minutes to bake</p>
          <p>Simple but tasty, this classic nut roast is very good with any of our gravy recipes. </p>
          <p>1 onion<br>
            3 tbsp vegetable oil<br>
            2 small tomatoes<br>
            30g/1oz wholemeal flour<br>
            140ml/5fl oz water<br>
            1 tbsp soya sauce<br>
            115g/4oz hazelnuts<br>
            115g/4oz broken cashew nuts<br>
            85g/3oz wholemeal breadcrumbs<br>
            1 tsp dried mixed herbs <br>
            2 tbsp soya flour, available from health food shops</p>
          <ol start="1" type="1">
            <li>Score the tomato skins, place in a cup and      top with boiling hot water.</li>
            <li>Chop the onion finely and sauté it in the      oil until tender.</li>
            <li>Preheat the oven to 350ºF/180°C/Gas Mark      4. </li>
            <li>Skin and chop the tomatoes and add them to      the pan. Cook for 5 minutes.</li>
            <li>Stir in the flour and slowly add the      water, stirring constantly to avoid lumps. Remove from the heat.</li>
            <li>Grind the hazelnuts and add them to the      sauce together with all the other ingredients. Mix very thoroughly and      place in a greased bread tin or pie dish.</li>
            <li>Bake in a moderate oven for 45 minutes to      1 hour.</li>
            <li>Serve with gravy and seasonal vegetables.</li>
          </ol>
          <p>&nbsp;</p>
          <p>Taken from <em>Easy Vegan Cooking</em> by Leah Leneman - available from Viva! £8.99</p>
