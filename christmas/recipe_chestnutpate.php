          <h1>Recipes</h1>
          <h2><strong>Chestnut Paté en Crôute </strong></h2>
          <p>            Serves 6. <strong></strong></p>
          <p>Approximately 55 minutes: 5-10 minutes preparation; 13 minutes cooking; 25-30 minutes baking time. <br>
            Thanks again to Rose Elliot, the Goddess of Veggie food, for the lovely recipe! This is <em>so </em>easy – and it frees you up to do other things while it’s baking. Serve with piles of roast potatoes, butternut squash and parsnips - plus one of our delicious gravy recipes and all the usual trimmings…<br>
            4 onions, chopped <br>
            2 sticks of celery, finely chopped <br>
            2 tablespoons olive oil <br>
            4 garlic cloves, crushed <br>
            100g/3½oz button mushrooms, sliced <br>
            435g can unsweetened chestnut puree <br>
            75g/3oz soft breadcrumbs <br>
            2 tbsp brandy <br>
            Salt and pepper <br>
            375g frozen ready-rolled puff pastry sheets (Jus-Roll and Brake Brothers are both vegan) <br>
            Soya milk to glaze </p>
          <ol start="1" type="1">
            <li>Fry the      onions and celery in the oil in a large saucepan, covered, for 10 minutes. </li>
            <li>Add the      garlic and mushrooms, cook for 2-3 minutes. </li>
            <li>Mix in the      chestnut puree, breadcrumbs, brandy and seasoning. </li>
            <li>Preheat      the oven to 230°C/450°/Gas Mark 8. </li>
            <li>Put the      pastry on a baking sheet. Pile the chestnut mixture lengthways down the      middle third – you may need more than one sheet of pastry. </li>
            <li>Make      diagonal cuts 1cm/½ inch apart on the pastry on either side of the      chestnut mixture. </li>
            <li>Fold these      up alternately to make a lattice covering it. </li>
            <li>Trim the      ends – you could make leaves and stick on top with water. </li>
            <li>Brush with      soya milk. </li>
            <li>Bake for 5      minutes, then reduce the setting to 200°C/400°F/Gas Mark 6 and bake for a      further 20-25 minutes. </li>
          </ol>
