<h2><strong>Rose's Gravy</strong></h2>

<p>30 minutes total preparation and cooking time. <br> <br>
<img src="/christmas/images/med/med/gravy1_med_.jpg" alt="" align="right" style="margin-left:10px;">
</p>

<p>
1 onion, diced <br>
2 tbsp oil <br>
2 tbsp plain flour <br>
Clove of garlic <br>
450ml/15 fl oz vegetable stock <br>
1tsp yeast extract <br>
1 or 2 tbsp soya sauce <br>
Freshly ground black pepper <br>
</p>

<table cellpadding="0" cellspacing="0" border="0">
<tr><td>
<ol start="1" type="1">
<li>

<img src="/christmas/images/med/med/gravy6_med_.jpg" alt="" align="right" style="margin-left:10px;">
Peel and crush the garlic, using a garlic press, or chop by hand.

</li>
</ol>
</td></tr>

<tr><td><ol start="2" type="1"><li>

<img src="/christmas/images/med/med/gravy7_med_.jpg" alt="" align="right" style="margin-left:10px;">
Fry the onion in the oil for five minutes.

</li></ol>
</td></tr>


<tr><td><ol start="3" type="1"><li>

<img src="/christmas/images/med/med/gravy8_med_.jpg" alt="" align="right" style="margin-left:10px;">
Add the flour and cook for a further five to ten minutes until the flour and onion are nut- brown and the onion is soft and slightly pulpy.

</li></ol>
</td></tr>    

<tr><td><ol start="4" type="1"><li>

<img src="/christmas/images/med/med/gravy9_med_.jpg" alt="" align="right" style="margin-left:10px;">
Add the garlic then gradually stir in the vegetable stock (or the water from any vegetables you happen to be boiling at the same time).

</li></ol>
</td></tr>  
<tr><td><ol start="5" type="1"><li>

Bring to the boil and simmer for ten minutes.

</li></ol>
</td></tr>   
<tr><td><ol start="6" type="1"><li>

Add the yeast extract, soya sauce and black pepper.

</li></ol>
</td></tr>  

</li></ol>
</td></tr>   
<tr><td><ol start="7" type="1"><li>

Stir well. Strain if you like, or serve as it is.

</li></ol>
</td></tr>  
