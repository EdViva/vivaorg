          <h1>Recipes</h1>
          <h2><strong>Cashew Nut Roast with a Choice of Two Stuffings </strong></h2>
          <p>            Serves 6-8<br>
            70 minutes total: 30 minutes preparation and cooking time, 40 minutes baking. </p>
          <p>I’ve cooked this every Christmas for the past few years now and everyone loves it, whatever their dietary persuasion! Serve with roast potatoes, mixed vegetables such as carrots, broccoli and sprouts - and gallons of gravy. Use your own favourite gravy recipe or try our delicious Adapted from the wonderful Vegan Family House website<a href="http://www.veganfamily.co.uk/">www.veganfamily.co.uk</a><br>
            30g/1oz margarine <br>
            2 sticks celery, finely chopped <br>
            1 medium leek, finely chopped <br>
            360ml/12fl oz hot water <br>
            1 tsp yeast extract (Marmite or supermarket own brand etc) <br>
            450g/16oz ground cashew nuts (or other nuts of your choice, including a mixed selection) NB health stores often sell cashew pieces which are much cheaper than buying them whole. &nbsp;<br>
            2 tbsp soya flour (available from health food shops)<br>
            2 tsp fresh herbs or1 tsp dried <br>
            160g/6oz white bread crumbs <br>
            Sea salt and pepper to taste </p>
          <p><em>Garlic Mushroom Stuffing</em><br>
            200g/7oz mushrooms, sliced (not too thick as they need to be sandwiched in between layers of nut roast)<br>
            1-2 cloves garlic<br>
            1 dsp olive oil<br>
            1 tbsp soya sauce</p>
          <p><em>Smoked Tofu, Red Pepper and Garlic Stuffing </em><br>
            1-2 cloves garlic<br>
            1dsp olive oil<br>
            ½ pack smoked tofu, cut into medium chunks<br>
            1 large red pepper, cut into small pieces<br>
            3 tbsp finely chopped parsley</p>
          <ol start="1" type="1">
            <li>Make the stuffing of your choice: </li>
            <ol start="1" type="a">
              <li><em>Mushroom</em>. Cook garlic in olive       oil until golden. Add mushrooms and cook for a few minutes. Add soya       sauce. Let this cook gently, then drain off any liquid that’s left and       reserve for gravy. Put mushroom garlic mix aside. </li>
              <li><em>Smoked Tofu</em>. Cook garlic as       with Mushroom filling. Add red pepper and sauté until soft – about 5       minutes. Add smoked tofu and cook for another few minutes. Mash up/gently       blend the vegetable/tofu mixture so it’s like a chunky paste. Remove from       heat and stir in parsley. Put mix aside.&nbsp; </li>
            </ol>
            <li>Melt the margarine (in a large pan for mixing) and cook the      celery and leek in it for a few minutes. </li>
            <li>Preheat oven to180°C/360°F/Gas Mark 4.</li>
            <li>Mix the yeast extract into the hot water (alternatively you      could use any stock you like) and add this to the leek and celery. </li>
            <li>Stir in the soya flour, nuts, herbs, breadcrumbs, salt and      pepper and mix well. Allow to cool slightly while you grease a loaf tin. </li>
            <li>Place half the nut roast mixture in the tin and press down well      - then add the stuffing (pressing down well again) and place the rest of      the nut roast mixture on top. </li>
            <li>Bake in the oven for about 40-50 minutes (check after 40 and      make sure it’s not getting too brown on the top). </li>
            <li>Allow to cool before turning out of the tin and slicing – you      can warm the slices up again before serving. </li>
          </ol>
