          <h1>Recipes</h1>
          <h2><strong>Viva!’s Deluxe Chestnut, Port and Thyme Strudel</strong></h2>
          <p>Serves 6<br>
              <br>
            Preparation/cooking time 1 hour</p>
<h3>Pastry</h3>
<p>1 packet puff pastry, defrosted</p>  
<a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-ingredients.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-ingredients.jpg" alt="Incredients laid out before we start" align="right" style="margin:4px;"></a><h3>Filling</h3>
<p>1 large onion, finely chopped<br>
3 cloves garlic, finely chopped<br>
2 sticks celery, finely sliced<br>
450g/1lb chopped nuts<br>
<a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-chestnut.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-chestnut.jpg" alt="Chopping the chestnuts" align="left" style="margin:4px;"></a>1 packet vacuum-packed pre-cooked chestnuts, chopped<br>
1 tin of chestnut puree<br>
60g/2oz breadcrumbs<br>
Big pinch thyme<br>
4 tbsp/60ml port<br>
1 tbsp soya milk<br>
</p>
<h3 style="clear:both;">Sprout mixture</h3>
<p>10 Brussels sprouts<br>
1 tbsp Pure margarine or other dairy-free spread<br>
Ground black pepper to taste</p>
<h3>Method</h3>
<p>


1.	Pre heat oven to 180°C/350° F/Gas Mark 4 <br><br><a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-mix.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-mix.jpg" alt="Mixing the ingredients together" align="absmiddle" style="margin:3px;" height="150px"></a><a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-mix-chestnuts.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-mix-chestnuts.jpg" alt="Mixing the ingredients together" align="absmiddle" style="margin:3px;" height="150px"></a><a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-cook.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-cook.jpg" alt="Mixing the ingredients together" align="absmiddle" style="margin:3px;" height="150px"></a><br><br>
2.	Lightly fry the onions, garlic and celery until soft.<br><br>
3.	Add the rest of the filling ingredients and stir.<br><br>
4.	Boil sprouts until soft then mash with margarine and pepper. <br><br>
<a href="/christmas/images/large/christmas-strudel-roast-nut-foil.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-foil.jpg" alt="Rectangle of filling on baking foil" align="right" style="margin:3px;"></a>5. Line a baking tray with foil, grease and mould half the filling into a rectangle. <br><br>
6.	Using a spoon, make an indent down the centre, spoon in and press down the sprouts.<br><br>
7.	Layer the rest of the filling on top and smooth off with pallet knife into a rectangle.  <br><br>
8.	Bake for 20 mins.<br><br>
9. Meanwhile flour your work surface and rolling pin and roll pastry thin into a rectangle, three times the width of the filling.<br><br>
<a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-pastry.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-pastry.jpg" alt="Rectangle of filling on baking foil" align="right" style="margin:3px;"></a>10. Using two food slices, transfer the filling into centre of pastry and cut neatly around it, removing what remains in whole pieces. <br><br>
11. Cut this excess pastry into long strips and layer across the top of the strudel, brushing with soya milk to attach ends to the strudel’s edges and glaze. Repeat in the other direction to create a lattice.<br><br>
<br>12.	Decorate with holly and berries shaped from spare pastry.<br><br>
13.	Make for 20 mins until pastry is golden.<br><br><a href="/christmas/images/large/christmas-strudel-roast-nut-pastry-nuts.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-pastry-nuts.jpg" alt="Transfer the filling into centre of pastry" align="absmiddle" style="margin:3px;" height="110px"></a><a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-brush.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-brush.jpg" alt="Brushing soya milk" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/christmas-strudel-roast-nut-sprouts-lattice.jpg" rel="gb_imageset[Viva!’s Deluxe Chestnut, Port and Thyme Strudel]"><img src="/christmas/images/med/med/christmas-strudel-roast-nut-sprouts-lattice.jpg" alt="Rectangle of filling on baking foil" align="absmiddle" style="margin:3px;" height="110px"></a>
