          <h1>Recipes</h1>
          <h2><strong>Chocolate &amp; Brandy Truffle Torte </strong></h2>
          <p>Serves 8<br>
              <br>
            20 minutes plus overnight chilling time</p>
          <p>Not only is this sophisticated little number a piece of chocolate heaven, it’s also incredibly quick and easy.</p>
          <p>Cooking oil or low-cal spray<br>
            5tbsp liquid glucose (Supercook brand available from large supermarkets or else chemists and some&nbsp; health food stores)<br>
            5tbs brandy<br>
            450g/1lb plain chocolate<br>
            250ml/½ pint tub soya cream (1 small tub)<br>
            75g/3oz regular Hob Nobs or similar oaty vegan biscuits <br>
            Sieved cocoa powder to decorate<br>
            Optional: raspberries or other berry fruit to garnish</p>
          <ol start="1" type="1">
            <li>Base line a 23cm/9in cake tin with greaseproof paper and brush      sides with oil or squirt with low-cal spray. </li>
            <li>Sprinkle the crushed biscuits over.</li>
            <li>Use a warm spoon to measure the glucose syrup.</li>
            <li>Melt chocolate, brandy and glucose in a bowl over a pan of      barely simmering water.</li>
            <li>Remove from heat and gently stir in cream. </li>
            <li>Pour into tin and chill overnight.</li>
            <li>Turn out onto a large plate and dust with cocoa powder. Garnish      with fruit if using.</li>
          </ol>
