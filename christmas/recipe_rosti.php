          <h1>Recipes</h1>
          <h2><strong>Parsnip &amp; potato rosti with 'cream cheese' and cranberries</strong></h2>
          <p>            Serves 6<br>
            50 minutes total preparation and cooking time.<br>
            Mmm… this little gourmet job is actually dead simple as well as tasting lovely – but don’t let on to your highly-impressed guests! Just remember to defrost the filo pastry in time – check out the packet instructions the night before.</p>
          <p>1 tbsp olive oil, plus more for brushing<br>
            3 shallots, chopped finely<br>
            2 cloves garlic, crushed<br>
            250g/9oz mixed mushrooms (shiitake, chestnut, etc.), chopped<br>
            Salt and freshly ground black pepper<br>
            250g/9oz firm tofu<br>
            1 tbsp soy sauce<br>
            50g/2oz ground walnuts<br>
            1 tin (390g) artichokes, drained and roughly chopped<br>
            2 tbsp fresh tarragon leaves, chopped<br>
            1 pack of filo pastry (250g)</p>
          <p>3 tbsp cranberry sauce<br>
            2 tangerines</p>
          <ol start="1" type="1">
            <li>Heat the olive oil and sauté the finely chopped shallots until      translucent. Add the garlic and mushrooms and season with salt and pepper. </li>
            <li>Cook over a medium heat until the mushrooms release their      liquid. Continue cooking until the liquid has evaporated.</li>
            <li>Crumble the tofu into a bowl (or mash it roughly with a fork)      and add the soy sauce, ground walnuts, chopped artichokes, tarragon and      the cooked mushrooms. Combine well and season with salt and pepper.&nbsp; </li>
            <li>Divide roughly into six portions and set aside to cool.</li>
            <li>Preheat the oven to 180˚C/350˚F/Gas Mark 4 and line a baking      tray with non-stick baking paper.</li>
            <li>To make individual strudels use two sheets of filo pastry for      each strudel. Remove only two sheets at a time from the pack and keep the      rest covered to stop the pastry from drying out.</li>
            <li>Place one sheet on a flat surface with the short side facing      you and brush lightly with olive oil. Lay the second sheet on top and      brush with oil too. Now, fold the bottom third of the pastry upwards.&nbsp; Place one portion of the filling      on this doubled up third, leaving a 5 cm/2 inch border all around. </li>
            <li>Fold the left and right sides in to encase the filling and      start rolling the strudel up (like a spring roll). </li>
            <li>Place the strudel seam side down on the prepared baking tray.      Continue with the remaining five strudels and brush the tops lightly with      oil.</li>
            <li>Bake for 30 minutes or until golden brown. </li>
            <li>Just before the strudels are ready for serving, squeeze the      juice from the tangerines and combine it in a small pot with the cranberry      sauce. Heat over a medium flame until warmed through and serve with the      strudels. </li>
          </ol>
          Tastes delicious with roasted potatoes and all the usual trimmings.
