<h2><strong>
<img border="0" src="http://www.viva.org.uk/christmas/images/AsparagusSpears.jpg" align="left" hspace="30" vspace="7" width="250" height="346">Asparagus 
and Cheatin&#39; <br>
Meatie Spears<br>
with vegan hollandaise sauce</strong></h2>

        <p >12 fat spears of asparagus<br>
        30g vegan margarine<br>
        1 packet Redwood Cheatin Pepperoni Style slices or Ham Style slices</span></p>
        <ol style="margin-top: 0cm; margin-bottom: 0cm" start="1" type="1">
          <li >Pre heat oven to 
          180C/350F/Gas Mark 4</span></li>
          <li >Cut Cheatin slices into 
          strips and wrap around the asparagus</span></li>
          <li >Brush each spear lightly with 
          margarine</span></li>
          <li>Cook for 3-4mins until 
          lightly roasted</span></li>
          <li >Serve with vegan Hollandaise 
          Sauce</span></li>
        </ol>
        <p ><b>&nbsp;Vegan Hollandaise Sauce</span></b></p>
        <p >6 pepper corns<br>
        1 dried bay leaf<br>
        40g silken tofu <br>
        125g/4oz vegan margarine<br>
        1tsp tumeric<br>
        lemon juice, salt and pepper to taste</span></p>
        <p >1. Put the vinegar in a small 
        pan with the peppercorns and bay leaf. Reduce the vinegar over a high 
        heat until there is only 1 tbsp left. Strain the peppercorns and the bay 
        leaf from this reduction.<br>
        2. Put the silken tofu in a food processor with the vinegar reduction.<br>
        3. Gently melt the marg so the solids fall to the bottom of the 
        saucepan.<br>
        4. Add the tumeric and turn the food processor on and slowly pour the 
        marg on to the tofu with the motor still running. The sauce will start 
        to thicken. When only the butter solids are left, stop.<br>
        5. If the sauce is too thick, add a little hot water.<br>
        6. Season to taste with salt and pepper and a little lemon juice.</span></p>

