          <h1>Recipes</h1>
          <h2><strong>Christmas Squash </strong></h2>
          <p>            Serves 4</p>
          <p>60 minutes total – while the squash is baking you can make the filling and sauce – easy. (Oh, and maybe quaff a festive glass of wine while you’re at it…) </p>
          <p>One of our friends from Solent Veggies &amp; Vegans cooked this dish for some meat-eating friends and they absolutely loved it. He was so proud! </p>
          <p>2 small squashes (eg acorn or small butternut squash)<br>
            3 cloves garlic<br>
            Olive oil<br>
            1 large sprig of fresh rosemary<br>
            Salt and freshly ground black pepper</p>
          <p>Filling:<br>
            A little olive oil<br>
            1 onion, finely chopped<br>
            7-10g (about half a packet) dried mushrooms (porcini or mixed)<br>
            70g/2½oz long grain rice<br>
            20g/scant 1oz wild rice<br>
            500ml/18fl oz vegetable stock<br>
            ½ tsp chopped rosemary<br>
            2 tsp dried cranberries<br>
            1 tbsp pine nuts<br>
            1 heaped tbsp fresh thyme leaves, chopped</p>
          <ol start="1" type="1">
            <li>Preheat the oven to 200˚C/400˚F/Gas Mark 6. </li>
            <li>Cut the squashes in half; remove all seeds with a spoon and      brush lightly with olive oil. </li>
            <li>Quarter the garlic cloves and place some in the hollows and dot      the rest around the edges of each squash half. </li>
            <li>Add the rosemary needles and season with a little salt and      freshly ground pepper.</li>
            <li>Place in a roasting dish and roast for 45 minutes until the      flesh is soft. </li>
            <li>While the squashes roast, prepare the filling: Sauté the onion      until translucent and add the wild rice and vegetable stock. </li>
            <li>Bring to the boil, then reduce the heat and simmer for 10      minutes. </li>
            <li>Add the dried mushrooms, long grain rice, rosemary and dried      cranberries. </li>
            <li>Bring to the boil once more, then cover and simmer over a very      low heat until the stock is absorbed and the rice is soft (about 20      minutes). </li>
            <li>Stir in the pine nuts and chopped thyme.</li>
            <li>Meanwhile, make the Divide the rice filling between the four      squash halves and return to the oven for 5 minutes. </li>
            <li>Serve with Roasted Red Pepper Sauce gently warmed and Christmas      vegetables of your choice. </li>
          </ol>
