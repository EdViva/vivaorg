<h2><strong>Little Vegan Yorkshire Puddings</strong></h2>

        <p class="MsoNormal">3oz/ 85g vegan margarine<br>
        4oz/ 115g self raising flour, sieved <br>
        pinch salt <br>
        280ml/ ½ pint soya milk<br>
        2oz/ 55g whole egg replacer<br>
        60ml/ 2 fl oz<br>
        1 tsp baking powder</p>
        <p class="MsoNormal">1. Preheat the oven to 220°C/425°F/Gas Mark 7.<br>
        2. Put 1 tsp vegan marg in each compartment on a bun tray with 9 
        compartments. <br>
        3. Mix the flour and salt with a hand whisk very thoroughly. Mix the egg 
        replacer into a smooth paste with the water. Mix this in with the milk 
        in a jug.<br>
        4. Place bun tray in oven for 10 mins so the vegan marg gets very hot.<br>
        5. Then add half of the egg replacer liquid to the dry mix. Mix it into 
        a smooth batter (paste) then add the remaining liquid. <br>
        6. Transfer the batter to a jug and fill each compartment to the top. It 
        should sizzle as it goes in. Cook for 15-20 mins.</p>

