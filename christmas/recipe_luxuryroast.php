          <h2><strong>Luxury Festive Roast</strong></h2>
          <p>Serves 6-8<br></p>
          <p>Probably the tastiest Christmas Roast you will ever eat! Teaming with juicy nuts pulses, porcini mushrooms and other surprises.<br><br>
            230g/8oz mixed nuts<br>
            4oz red lentils <br>
            l large carrot <br>
            2 celery sticks <br>
            1 large onion<br>
            ½  large courgette<br>
            1 aubergine<br>
            100g mushrooms <br>
            25g porcini mushrooms <br>
            4 tbsp vegan butter<br>
            2 tbsp mild curry powder<br>
            2 tbsp tomato ketchup<br>
            2 tbsp Vegan Worcestershire sauce<br>
            4 tbsp chopped parsley	<br>
            10g chopped dried apricots <br>
            2tbsp soya flour<br>
            ¼ pint water<br>
            Vegan pesto<br>
            2 tbsp toasted pine nuts<br>          
            </p>
          <p>Preheat oven to 190C/375F/Gas 5 <br></p>
          <table cellpadding="0" cellspacing="0" border="0">
          <tr><td>
          <ol start="1" type="1">
            <li>Gently boil the lentils for around 20mins and soak the porcini mushrooms in boiled water for the same time.</li>
            </ol>
          </td></tr>
          <tr><td>
          <ol start="1" type="1">
            <li>At the same time, cut the aubergine in half, then chop into small chunks, along with the courgette. Lay out on a plate and spinkle with salt (this is to reduce their bitterness). Leave for 20mins also.</li>
            </ol>
          </td></tr>
          
          <tr><td><ol start="2" type="1"><li><img src="/christmas/images/med/med/special_roast10_med_.jpg" align="right" style="margin-left:10px;">At the same time, cut the aubergine in half, then chop into small chunks, along with the courgette. Lay out on a plate and spinkle with salt (this is to reduce their bitterness). Leave for 20mins also.</li></ol>
          </td></tr>    
          
                    <tr><td><ol start="3" type="1"><li>Crop the carrot, celery, onion and mushrooms into small chunks, then pass them through a food processor or blender until they are quite finely chopped.<br><img src="/christmas/images/med/med/special_roast16_med_.jpg" align="left" style="margin:10px;"><img src="/christmas/images/med/med/special_roast24_med_.jpg" align="left" style="margin:10px;"></li></ol>
          </td></tr>  
          
          <tr><td><ol start="4" type="1"><li>Then melt the butter in a wok and fry the vegetables for 5 mins, stirring in the curry powder<br><img src="/christmas/images/med/med/special_roast28_med_.jpg" align="left" style="margin:10px;"><img src="/christmas/images/med/med/special_roast20_med_.jpg" align="left" style="margin:10px;"></li></ol>
          </td></tr>  
          
 <tr><td><ol start="5" type="1"><li><img src="/christmas/images/med/med/special_roast32_med_.jpg" align="right" style="margin-left:10px;">Drain and crop the porcini mushrooms, then add into a bowl with the lentils, ketchup, Worcestershire sauce, parsley, apricots, soya flour and water. Mix well. </li></ol>
          </td></tr>    
          
 <tr><td><img src="/christmas/images/med/med/special_roast37_med_.jpg" align="right" style="margin-left:10px;margin-top:10px;">
 <ol start="6" type="1"><li>Add in mixture from wok and mix all together.</li><br><br><br>
 <li>Grease a large bread tin with vegan butter, then line with grease-proof paper.</li>
 </ol>
</td></tr> 
              
 <tr><td>
 <ol start="8" type="1"><li>Press 4 tbsp of the mixture into the tin, then spread over a layer of pesto. Spoon in the rest of the mixture and smooth over.</li><br><br><br>
 <li>Bake for about 1 hour until just firm, covering the top with a piece of grease-proof paper if it starts to burn.</li>
 </ol>
</td></tr> 

          <tr><td><ol start="10" type="1"><li>Sprinkle with pinenuts to finish, then holding the sides of the grease-proof paper, gently pull the loaf out onto a plate or serving tray. Trim down the paper along the edges, allow to cool for 15 mins, then cut into slices and serve with Roses' Gravy.<br><img src="/christmas/images/med/med/special_roast40_med_.jpg" align="left" style="margin:10px;"><img src="/christmas/images/med/med/special_roast48_med_.jpg" align="left" style="margin:10px;"></li></ol>
