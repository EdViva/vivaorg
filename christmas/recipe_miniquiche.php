<h2><strong>Mini Cherry Tomato and Vegan Pesto 'Quiches'</strong></h2>

<p>Serves 6</p>

<p>
1 packet of Just Roll ready made shortcrust tartlet cases (available in Sainsbury’s and other supermarkets)<br>
Punnet of cherry tomatos<br>
½ courgette, chopped into small pieces<br>
Jar of vegan pesto<br>
1 packet of silken tofu<br>
½ cup soya milk<br>
Vegan ‘cheese’ such as Sheese<br>
Salt and pepper<br>
Pre-heat oven to 180C<br>
</p>






<table cellpadding="0" cellspacing="0" border="0">
<tr><td>
<ol start="1" type="1">
<li>
<img src="/christmas/images/med/med/quiche1_med_.jpg" alt="" align="right" style="margin-left:10px;">

Place the tartlets on a baking tray. Using a pallet knife, spread a tbsp of vegan pesto around the base of each tartlet.
</li>
</ol>
</td></tr>
<tr><td><ol start="2" type="1"><li>

<img src="/christmas/images/med/med/quiche6_med_.jpg" alt="" align="right" style="margin-left:10px;">
Lightly brown courgette in olive oil, in a small frying pan, then distribute the pieces evenly in each pastry case, between two cherry tomatoes. 

</li></ol>
</td></tr>    

<tr><td><ol start="3" type="1"><li>

<img src="/christmas/images/med/med/quiche9_med_.jpg" alt="" align="right" style="margin-left:10px;">
For the filling, using a food processor, or hand-blender, blend the tofu and soya milk until smooth.

</li></ol>
</td></tr>  
<tr><td><ol start="4" type="1"><li>

<img src="/christmas/images/med/med/quiche13_med_.jpg" alt="" align="right" style="margin-left:10px;">

Spoon the tofu mixture into the cases, so all the filling is evenly covered. Using a hand grater, grate vegan ‘cheese’ over the top of each quiche.


</li></ol>
</td></tr>   
<tr><td><ol start="5" type="1"><li>

Place the baking tray of quiches into the oven for approximately 30 mins, until they have browned off nicely.

</li></ol>
</td></tr>  

</li></ol>
</td></tr>   
<tr><td><ol start="6" type="1"><li>

Gently remove from the cases by tipping onto out onto a saucer, placing a second saucer on the bottom and turning. Serve in pieces, or as whole mini quiches.
