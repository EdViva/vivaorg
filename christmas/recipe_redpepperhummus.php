          <h2><strong>Red Pepper Hummus with Smoked Paprika </strong></h2>
          <p>Serves 2-4</p>
          <p> 5 minutes preparation plus blending time. <br>
            Another Rose Elliot delight. This is a stylish and delicious variation on an old favourite, as well as being super-fast.<br>
            2 garlic cloves <br>
            425g/14oz can chick peas, drained <br>
            Half a 320g jar whole sweet red peppers, drained <br>
            1 tsp sugar or maple syrup <br>
            Hot pepper sauce, eg Tabasco<br>
            ¼-½ tsp smoked paprika – available from large supermarkets, delis or health food shops<br>
            Coarsely ground or cracked black pepper <br>
            To serve <br>
            Warm pitta bread, crostini or chunks of raw vegetables such as carrot, celery, green pepper </p>
          <ol start="1" type="1">
            <li>Whizz the      garlic cloves in a food processor until chopped then put in the chick      peas, sweet red pepper and maple syrup or sugar and whiz again. </li>
            <li>Stir in a      drop or two of hot pepper sauce and smoked paprika to taste. </li>
            <li>Turn the      mixture onto a flat plate and smooth the surface. </li>
            <li>Grind some      black pepper coarsely over the top. </li>
            <li>Serve with      strips of warmed pitta bread or other suggested accompaniments.</li>
          </ol>
