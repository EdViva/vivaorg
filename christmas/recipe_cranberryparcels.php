          <h2><strong>Helen’s Cream 'Not Cheese' and Cranberry Parcels</strong></h2>
          <p>Serves 6<br>30 mins<br></p>
          <p>A real delight as a starter for your Christmas table.<br><br> ¼ tub vegan 'cream cheese' like Toffutti<br>
    225g/8oz packet fresh or frozen filo pastry<br>
    25g/1oz vegan margarine, melted<br>
    30ml/6tsp cranberry sauce<br>
   
            </p>
          <table cellpadding="0" cellspacing="0" border="0">
          <tr><td>
          <ol start="1" type="1">
            <li>Pre-heat the oven to 190°C/375°F/Gas Mark 5.</li>
            </ol>
          </td></tr>
          <tr><td><ol start="2" type="1"><li><img src="/christmas/images/med/med/squares1_med_.jpg" align="right" style="margin-left:10px;">Take 3 sheets of filo pastry at a time. Cut in half so you have 10 12.5cm/4-5 inch squares. Take one square and brush with a little melted butter or margarine. Place another square on top at an angle, and brush again. Do the same with a third sheet.</li></ol>
          </td></tr>    
          
                    <tr><td><ol start="3" type="1"><li>Place a teaspoon of cream 'cheese' in the middle of the filo pastry and top with a teaspoon of cranberry sauce. Bring the edges of the pastry up together and pinch them to form a little parcel (money-bag shape). Make up the rest of the parcels in the same way.<br><img src="/christmas/images/med/med/squares2_med_.jpg" align="left" style="margin:10px;"><img src="/christmas/images/med/med/squares3_med_.jpg" align="left" style="margin:10px;"></li></ol>
          </td></tr>  
          
           <tr><td><ol start="4" type="1"><li><img src="/christmas/images/med/med/squares6_med_.jpg" align="right" style="margin-left:10px;">Brush with melted vegan margarine and place on a greased baking tray.</li></ol>
          </td></tr>   
          
           <tr><td><ol start="5" type="1"><li><img src="/christmas/images/med/med/squares10_med_.jpg" align="right" style="margin-left:10px;">Bake in the pre-heated oven for 15-20 minutes until the pastry is crisp and golden.
Serve hot with extra cranberry sauce.
</li></ol>
          </td></tr>  
          
                     <tr><td>Tip – try experimenting with different fillings in these little parcels – like spinach and mashed tofu with paprika; vegan pesto, cherry tomatoes and cream ‘cheese’ or garlic mushrooms for example.
          </td></tr>  
          
