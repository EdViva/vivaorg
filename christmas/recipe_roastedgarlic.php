          <h1>Recipes</h1>
          <h2><strong>Roasted Garlic Heads with Thyme </strong></h2>
          <p>Serves 6<br>
              <br>
            90 minutes, including soaking time</p>
          <p>This lovely aromatic spread can also be spread on bread or toast as a starter.<br>
              <br>
            6 large heads of garlic<br>
            100ml/3½fl oz olive oil<br>
            8 sprigs fresh thyme<br>
            Salt and pepper</p>
          <ol start="1" type="1">
            <li>Soak heads of garlic in cold water for 30 minutes. </li>
            <li>Preheat oven to 80ºC/350ºF/Gas Mark 4.</li>
            <li>Drain and slice off the garlic tops or cut in half if large.</li>
            <li>Place in roasting tin with oil and thyme and season to taste. </li>
            <li>Roast for 50minutes-1hr, turning occasionally. </li>
            <li>Serve when golden and tender.</li>
          </ol>
