          <h1>Recipes</h1>
          <h2><strong>Nut-Stuffed Mushrooms </strong></h2>
          <p>Serves 4</p>
          <p>25 minutes </p>
          <p>5 very large flat mushrooms - 1 per person plus 1 extra mushroom for the stuffing- or 10 medium-large mushrooms if not available <br>
            1 large onion<br>
            2 garlic cloves<br>
            2 tsp olive oil<br>
            60g/2oz mixed unsalted nuts (brazils/hazelnuts/almonds/walnuts)<br>
            1 tbsp vegan margarine<br>
            ½ tsp dried thyme<br>
            ½ tsp dried rosemary<br>
            1 tbsp chopped fresh herbs eg parsley, fennel, dill, chives<br>
            Salt and pepper to taste.</p>
          <ol start="1" type="1">
            <li>Cut the stems from the mushrooms and      finely dice these along with one whole mushroom.</li>
            <li>Finely dice the onion and crush the garlic      cloves.</li>
            <li>Preheat the oven to 400°F/200°C/Gas Mark      6.</li>
            <li>Place the onion and garlic in a saucepan      with the olive oil and sweat them in the oil until the onion is soft and      beginning to brown. </li>
            <li>Add the chopped mushroom stalks and sweat      the vegetables for a few more minutes.</li>
            <li>Meanwhile, lightly toast the mixed nuts in      a medium oven or grill and remove the skins.</li>
            <li>Grind the mixed nuts fairly finely in a      food processor or using a mortar and pestle and add to the pan along with      the margarine, dried herbs, fresh herbs and salt and pepper to taste. Mix      to combine.</li>
            <li>Place the field mushrooms, stem side up,      on a baking tray. </li>
            <li>Divide the mixture between the mushrooms      and spread over the surface of each one.</li>
            <li>Bake in the oven for 5-8 minutes or until      the tops are beginning to brown and the mushrooms are just cooked. </li>
            <li>Serve a mushroom on each plate along with      a salad garnish.</li>
          </ol>
          <p>&nbsp;</p>
