          <h1>Recipes</h1>
          <h2><strong>Heavenly Halva Ice with Lemon Biscotti </strong></h2>
          <p>Serves 6<br>
            2 hours, including freezing time for the ice cream. </p>
          <p>A rather special dessert that can be prepared beforehand to save time – the ice cream can go in the freezer and the biscotti stored in an air-tight container<br>
              <em>Halva Ice Cream:</em><br>
            400g/14 oz pistachio halva<br>
            500g/18fl oz soya yoghurt<br>
            Icing sugar</p>
          <p><em>Biscotti:</em><br>
            75g/2½ oz margarine<br>
            125g/4½ oz sugar<br>
            250g/9oz self-raising flour<br>
            2 quantities of egg replacer (found in health food shops) ie enough to replace 2 eggs <br>
            1 tsp vanilla essence<br>
            1 tsp baking powder<br>
            Grated zest of 1 lemon <br>
            Flaked almonds</p>
          <p>To make the ice cream:</p>
          <ol start="1" type="1">
            <li>Chop the halva into small pieces. </li>
            <li>Combine these with the soya yoghurt and blend with a      hand-blender (or food processor) until smooth. </li>
            <li>Add a little icing sugar to taste.</li>
            <li>Transfer the mixture to a plastic container and freeze for 1½ -      2 hours. </li>
            <li>Blend again briefly to break up the ice crystals and return to      the freezer for another 2 hours.</li>
          </ol>
          <p>To make the biscotti:</p>
          <ol start="1" type="1">
            <li>Preheat the oven to 180˚C/350˚F/Gas Mark 4 and line a baking      tray with non-sick baking paper.</li>
            <li>Cream the margarine and sugar together until fluffy. </li>
            <li>Blend in the egg replacer and vanilla essence. </li>
            <li>Sieve in the flour and baking powder and add the lemon zest and      flaked almonds. </li>
            <li>Take a heaped teaspoon of the dough and roll it into a      ball.&nbsp; Place it on the baking      tray and flatten it slightly. </li>
            <li>Bake for 25 minutes or until golden and crisp – longer if      biscotti still soft. </li>
            <li>Store any extra biscotti in an airtight container.</li>
          </ol>
          <p>Biscotti recipe from <em>Vegan Planet</em>.by Robin Robertson, available from VVF</p>
