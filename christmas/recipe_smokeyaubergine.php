          <h1>Recipes</h1>
          <h2><strong>Smoky Aubergine &amp; Pepper Dip</strong></h2>
          <p>            Serves 6</p>
          <p>90 minutes roasting time, 5 minutes to blend </p>
          <p>Lovely, lovely – smooth, aromatic and with a divine roast and smokey flavour… . While the vegetables are baking you can just set the alarm and be doing other things – or put your feet up! Blending the cooked ingredients is a piece of cake. </p>
          <p>2 aubergines<br>
            1 clove garlic, crushed<br>
            4 tsp lemon juice<br>
            1 large red pepper<br>
            1 tbsp olive oil<br>
            ¼ tsp smoked paprika<br>
            Salt and freshly ground black pepper</p>
          <ol start="1" type="1">
            <li>Preheat the oven to 200˚C/400˚F/Gas Mark 6.</li>
            <li>Rub the aubergines and pepper with a little olive oil and place      them in an ovenproof dish. </li>
            <li>Roast the pepper for 1 hour and the aubergines for 1½ hours,      turning them occasionally, until the skins have turned black. </li>
            <li>Place the pepper in a sealed bag or a covered bowl - the      released steam loosens the skin and makes it easier to peel.</li>
            <li>Once the aubergines and pepper have cooled down enough to      handle, remove the skins and discard the seeds. </li>
            <li>Combine with the crushed garlic, lemon juice, olive oil and      smoked paprika and whiz in a blender until smooth. </li>
            <li>Season with salt and black pepper and add a little extra lemon      juice to taste. </li>
            <li>Serve chilled or at room temperature with crackers, breadsticks      or crusty bread.</li>
          </ol>
          <p>&nbsp;</p>
          <p>Tip: To add a bit more bite to the dip, reserve a quarter of the pepper and aubergines and cut both into small cubes. Stir them into the creamy dip for a chunkier texture.</p>
