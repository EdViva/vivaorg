          <h1>Recipes</h1>
          <h2><strong>Vegetables à la Grecque </strong></h2>
          <p>            Serves 6-8</p>
          <p>25 minutes preparation and cooking time <br>
          </p>
          <p>Marinade:<br>
            ½ tsp black peppercorns<br>
            2 bay leaves<br>
            1 large fresh rosemary sprig<br>
            2 large fresh thyme sprigs<br>
            500ml/18fl oz dry white wine<br>
            500ml/18fl oz water<br>
            4 cloves garlic, crushed<br>
            125ml/4½fl oz cider vinegar<br>
            1 tsp salt</p>
          <p>Plus vegetables of your choice (cauliflower, artichokes, red peppers etc. 6 to 8 cups worth)</p>
          <ol start="1" type="1">
            <li>Tie all the fresh herbs together with a piece of kitchen string      and place them in a saucepan together with all the other marinade      ingredients. </li>
            <li>Bring to the boil, then reduce the heat and simmer covered for      15 minutes.</li>
            <li>In the meantime, slice and chop the vegetables you have chosen. </li>
            <li>Blanch each vegetable in the marinade until just tender. </li>
            <li>Remove it to a platter with a slotted spoon. Begin with blander      vegetables and end with the strongest flavoured ones. Thinly sliced      potatoes and carrots take about 12 minutes to cook, while mushrooms,      peppers, mange tout and courgette only take 3-4 minutes. </li>
            <li>Strain any remaining marinade over the cooked vegetables and      serve warm or chilled for later. </li>
            <li>Serve with crusty wholemeal bread.</li>
          </ol>
          <p>&nbsp;</p>
          Starter adapted from <em>Moosewood Restaurant Low-Fat Favourites</em>
