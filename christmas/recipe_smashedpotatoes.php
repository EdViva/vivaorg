<h2><strong>Smashed Seasonal Potatoes</strong></h2>

<p>
Seasonal potatoes <br>
Pinch of salt <br>
5tbsp olive oil <br>
1 jar pimento stuffed olives (drained and rinsed) <br>
2tbsp capers (drained and rised) <br>
8 sundried tomatoes <br>
Zest of 1 lemon <br>
Pre-heat oven to 180C <br>
</p>


<table cellpadding="0" cellspacing="0" border="0">
<tr><td>
<ol start="1" type="1">
<li>

<img src="/christmas/images/med/med/smash1_med_.jpg" alt="" align="right" style="margin-left:10px;">
Either wash or peel you potatoes and cut in half or quarter.

</li>
</ol>
</td></tr>
<tr><td><ol start="2" type="1"><li>

Place into a large saucepan and cover with cold water and salt, bring to boil, then turn down and simmer on a medium heat for approximately ten mins.

</li></ol>
</td></tr>    

<tr><td><ol start="3" type="1"><li>

After five mins, pour oil into a large shallow oven dish, and place in oven for five mins, to heat it through.

</li></ol>
</td></tr>  
<tr><td><ol start="4" type="1"><li>

Drain potatoes, remove dish from oven using oven gloves, then tip potatoes carefully in, shaking around so that all are coated with some oil.

</li></ol>
</td></tr>   
<tr><td><ol start="5" type="1"><li>

Place the potatoes into the oven for 30 mins.

</li></ol>
</td></tr>  

</li></ol>
</td></tr>   
<tr><td><ol start="6" type="1"><li>

<img src="/christmas/images/med/med/smash17_med_.jpg" alt="" align="right" style="margin-left:10px;">
Meanwhile, chop the olives and sundried tomatoes together on a chopping board, then transfer them into a mixing bowl. Add the capers and lemon zest, and mix.

</li></ol>
</td></tr>  
<tr><td><ol start="7" type="1"><li>

<img src="/christmas/images/med/med/smash15_med_.jpg" alt="" align="right" style="margin-left:10px;">
Remove the potatoes from the oven. They should be starting to go crispy. Smash the tops lightly with a folk, then spoon on all the prepared mixture.

</li></ol>
</td></tr>  
<tr><td><ol start="8" type="1"><li>

Return to the oven for another 15-20 mins, until golden and crispy.

</li></ol>
</td></tr>  

