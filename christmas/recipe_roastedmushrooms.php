          <h1>Recipes</h1>
          <h2><strong>Roasted Mushrooms with Onion Marmalade </strong></h2>
          <p>            Makes 12</p>
          <p>60-70 minutes, including preparation, cooking and baking time</p>
          <p>Mmm – gorgeous!&nbsp; This is good as a starter or as a side dish. </p>
          <p>1 tsp olive oil<br>
            2 medium red onions<br>
            2 tbsp balsamic vinegar<br>
            1 generous pinch of sugar<br>
            2 tsp redcurrant jelly<br>
            1 tbsp red wine (optional)<br>
            12 flat field mushrooms<br>
            3 tbsp olive oil<br>
            2 cloves garlic, crushed<br>
            ½ tsp mild paprika<br>
            1 tbsp fresh thyme leaves<br>
            1 tsp dried mixed herbs<br>
            Salt and pepper<br>
            Grated dairy-free cheese, eg melting mozzarella style (optional)<br>
            Rocket or other decorative salad leaves for serving</p>
          <ol start="1" type="1">
            <li>To make the marmalade, cut the      onions in half and then slice finely. </li>
            <li>Sauté in the tsp of oil over a      very low heat for 15-20 minutes, stirring occasionally. </li>
            <li>Add the vinegar, red wine, sugar      and redcurrant jelly. </li>
            <li>Simmer for another 5 minutes until      the onions start to caramelise, then set aside.</li>
            <li>Preheat the oven to 190˚C/375˚F/Gas      Mark 5. </li>
            <li>In a small bowl combine the olive      oil with the crushed garlic and herbs. Season with salt and pepper. </li>
            <li>Wipe the mushrooms clean with      kitchen paper and carefully remove the stalks. Place the mushrooms on a      non-stick oven tray and drizzle the seasoned olive oil over them. </li>
            <li>Roast for 25 minutes.</li>
            <li>Divide the onion marmalade between      the mushrooms and sprinkle with dairy-free cheese if using. </li>
            <li>Return the tray to the oven and      bake for a further 5 minutes. </li>
            <li>Serve on a bed of salad leaves with      any leftover onion marmalade and crusty bread</li>
          </ol>
