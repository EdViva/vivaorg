<p><strong>Rationale</strong></p>
<p>I saw the gap. The one I felt as a child. It existed between  Sunday school stories and my felt-relationship with nature as a basis for  personal spirituality. Over a lifetime this gap, this lack of  interconnectedness, the split-apartness went through many metamorphoses. Yet my  felt-relationship with nature was so strong I never fell into the agnostic or  atheistic trap! Instead I became interested in – and fascinated by – the  diversity of formal religious expression in peoples and cultures all-time;  indigenous to present day.<br />
  So I conceived of  PAN – the Personal Associative Network -   our individual neural networks <em>patterning</em> the way we see  stuff.  Above all, it forms the basis of  our worldview. A kaleidoscope is fine. Fragmented is not. Fragmented means  wholeness has gone off the radar.<br />
  That's how I came  to the point where I knew that for greater personal wholeness, I needed to  re-see my childhood through EcoCarols. It acted as a deep self-healing process,  taking on and working with the reverential driving all those human expressions  of love, joy,  awe, respect, passion,  compassion, honouring,devotion, wonder, gratitude, thankfulness,  celebration, courage, commitment - the full range of positive emotions.  Directing them towards the Earth and the future of our own species on this  planet.<br />
  EcoCarols are a  way to honour both – origin and the place I stand today.<br />
  To honour the source is important.. For me  personally, that was not really the source at all, of course, but a cultural  childhood conditioning. Always hard to shift.   The embedded partial patriarchal spirituality that weaned women and  their place out of the picture. With the single exception of the virgin-mother  rôle – that contradiction in terms that double-pleasures a certain type of man.  In doing this I gave myself the space to honour what I've always secretly  honoured, respected, loved and learnt from – the Earth in all her aspects,  lifeforms and processes.<br />
  I deeply believe  that when people see <em>energy</em> and <em>spirit</em> as one and the same thing  – conflate the two words for themselves, really get into it and what it means,  major dualities in our world will come crashing down.  Then we may attain a real brotherhood and  sisterhood in our world.<br />
  It is said that  it's bad luck to sing Christmas carols out of season. That being so, we can  sing EcoCarols year round and reserve the traditional versions for Christmas.<br />
  <br />
  <em>    'Reach deep into  the culture. Like plunging your hand into the Lucky Dip Bran Tub. Pull out the  tiny remnants of spirituality as yet still living. Hold them up to an  ecological light. This will result in a transformation before your very eyes,  one to celebrate.'</em></p>
