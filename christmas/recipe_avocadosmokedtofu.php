          <h1>Recipes</h1>
          <h2>Avocado with Smoked Tofu </h2>
          <p>Serves 4</p>
          <p>Under 10 minutes, including making the vinaigrette<br>
              <br>
            2 large, ripe avocados<br>
            1 packet smoked tofu (available from most health food stores)<br>
            2 dsp sesame seeds<br>
            Salt and black pepper<br>
  <br>
  <strong>To serve:</strong><strong><br>
</strong>Mustard vinaigrette </p>
          <ol>
            <li>Divide the avocados into halves, length-ways. Remove the stone and skin. Place each half on an individual serving plate, cut into 6 slices.</li>
            <li>Cut the smoked tofu into thin slices and insert in between avocado cuts.</li>
            <li>In a saucepan, dry roast the sesame seeds over a high heat until starting to brown.</li>
            <li>Allow to cool and sprinkle liberally over avocado and tofu halves.</li>
            <li>Season with salt and pepper and pour mustard vinaigrette round.</li>
          </ol>
          <p><strong>Mustard Vinaigrette</strong><br>
            This recipe makes more than you will need. You can halve the quantities if you wish - but it keeps for weeks if stored in the fridge in a screw-top jar and is good with other salads. </p>
          <p>175ml/6fl oz extra virgin olive oil<br>
            75ml/3fl oz white wine vinegar<br>
            Pinch of salt<br>
            Masses of black pepper<br>
            2 dsp prepared French or Dijon mustard </p>
          <ol start="1" type="1">
            <li>Mix ingredients in a jar with lid and shake until well combined      or place in a bowl and whisk.</li>
          </ol>
