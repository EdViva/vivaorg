          <h1>Recipes</h1>
         <a href="/christmas/images/large/cous-cous-vegan-vegetarian.jpg" title="PSprinkle pine nuts on the top and serve" rel="gb_imageset[Parcels]"><img src="/christmas/images/med/med/cous-cous-vegan-vegetarian.jpg" alt="Parcels" align="right" style="margin:7px;"></a> <h2><strong>Justine's Christmas Cous-Cous salad</strong></h2>
          <p>A tasty festive salad that is a treat for the eyes as well as the stomach! <br><br>This colourful salad can be served as part of a Christmas buffet or as a side dish with any of the main dishes listed in Viva!â€™s Very Veggie Christmas website. It would go especially well with the Aubergine Towers & Roast Tomato Sauce. <br>
<br>
Preparation/cooking time 1 hour</p>
<h3>Ingredients</h3>
<a href="/christmas/images/large/cous-cous-onion-corriander-red-yellow-pepper-almond.jpg" title="Justine's Christmas Cous-Cous salad - Ingredients" rel="gb_imageset[Parcels]"><img src="/christmas/images/med/med/cous-cous-onion-corriander-red-yellow-pepper-almond.jpg" alt="Parcels" align="absmiddle" style="margin:7px;"></a><a href="/christmas/images/large/cous-cous-pomegranite.jpg" title="Justine's Christmas Cous-Cous salad - Ingredients" rel="gb_imageset[Parcels]"><img src="/christmas/images/med/med/cous-cous-pomegranite.jpg" alt="Parcels" align="absmiddle" style="margin:7px;"></a><a href="/christmas/images/large/cous-cous-pomegranite-ingredients.jpg" title="Justine's Christmas Cous-Cous salad - Ingredients" rel="gb_imageset[Parcels]"><img src="/christmas/images/med/med/cous-cous-pomegranite-ingredients.jpg" alt="Parcels" align="absmiddle" style="margin:7px;"></a><br><p>250g/9oz cous cous<br>
250ml/9 fl oz boiling water<br>
2 tsp of harissa sauce (Belazu's rose harissa is best)<br>
1 red onion, chopped<br>
1 red pepper, chopped<br>
1 yellow pepper, chopped<br>
1 pomegranate (separate the seeds)<br>
50g/2oz toasted almonds<br>
Large sprig flat leaf parsley, chopped<br>
50g/2oz pine nuts, toasted<br>
</p>
<h3>Method</h3>
<p>

<a href="/christmas/images/large/cous-cous.jpg" title="Put the cous cous in a bowl and cover with boiling water, leave for 5 mins" rel="gb_imageset[Parcels]"><img src="/christmas/images/med/med/cous-cous.jpg" alt="Parcels" align="left" style="margin:7px;"></a>
1.	Put the cous cous in a bowl and cover with boiling water, leave for 5 mins<br>
2.	Plump up the cous cous with a fork<br>
3.	Stir in harissa sauce until evenly coloured<br>
4.	Stir in the onion, peppers, pomegranate, almonds and herbs<br>
5.	Transfer to a wide flat serving bowl<br>
6.	Sprinkle pine nuts on the top and serve<br>




