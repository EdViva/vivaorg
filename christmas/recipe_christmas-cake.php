          <h1>Recipes</h1>
          <h2>Big Boozy Yule Cake</h2>
          <p>A lovely, easy fruit cake that is good at just about any time of the year, although especially nice during the festive season.</p>
          <ul>
            <li>350g/12oz fine self-raising wholemeal flour OR 175/6oz each of fine self-raising wholemeal and half white self-raising </li>
            <li>1 tsp mixed spice </li>
            <li>175g/6oz vegan margarine OR 115ml/4fl oz sunflower or similar oil plus 60ml/2floz soya milk </li>
            <li>150g dark brown sugar – if very lumpy, sieve or grind up before adding </li>
            <li>500g/generous 1lb mixed dried fruit </li>
            <li>100g/4oz glacé cherries, dried and halved </li>
            <li>Grated zest of 1 large orange </li>
            <li>50g/2oz flaked almonds </li>
            <li>1 tbsp black treacle/molasses </li>
            <li>2 tbsp ground almonds </li>
            <li>125ml/4fl oz soya milk </li>
            <li>2 tbsp red wine OR cider vinegar </li>
            <li>¾ tsp bicarbonate of soda </li>
            <li>2 tbsp brandy</li>
          </ul>
          <ol>
            <li>Preheat oven  to 150ºC/300ºF/Gas Mark 2 and grease a 20cm/8 inch cake tin top and  bottom. Line the sides with a double layer of greased baking parchment.  NB reduce temperature a little if using a fan-assisted oven – about  5-10 degrees. </li>
            <li>Sift flour and mixed spice into a bowl. </li>
            <li>Rub margarine/oil into the flour with your fingers until the  mixture resembles fine breadcrumbs, then add sugar, dried fruit,  cherries, orange zest, flaked almonds, black treacle/molasses and  ground almonds. </li>
            <li>Warm half the soya milk in a small saucepan and add the vinegar. </li>
            <li>Dissolve the bicarbonate of soda (baking soda) in the rest of  the soya milk, then add this to the milk and vinegar mixture. It foams  up very dramatically! </li>
            <li>Quickly stir this into the flour and fruit, mixing well so that everything is combined. </li>
            <li>Spoon the mixture into the prepared cake tin and smooth out  the top. Cover top of cake with a double layer of baking parchment to  prevent burning. </li>
            <li>Bake in the preheated oven for 2-2½ hours, (check after 2  hours) until a skewer inserted into the centre of the cake comes out  clean and the centre of the cake springs back a little when pressed.  The secret is to make sure the cake is cooked in the middle but the  fruit hasn’t burned round the edges! </li>
            <li>Leave cake in tin to cool then turn out and strip off the  greaseproof paper. Place carefully on a cooling rack – a clean grill  pan rack will do. </li>
            <li>Prick cake all over with a cocktail stick and pour over 2 tbsp brandy. </li>
            <li>Wrap in baking parchment and store in a tin until required. It  will keep for 3-4 weeks, during which time you can ‘feed’ it with more  brandy, as above! </li>
            <li>It may also be covered with vegan marzipan and fondant icing shortly before serving, if desired.</li>
          </ol>
