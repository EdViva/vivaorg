          <h1>Recipes</h1>
          <h2><strong>Maple, Date &amp; Pecan Parcels</strong></h2>
          <p>Makes 8</p>
          <p>Easy – just remember to defrost filo pastry in plenty of time before you need it – check the packet instructions the night before. </p>
          <p>8 sheets of filo pastry (make sure sheets are no bigger than 30cm x 20cm (12” x 8”)<br>
            1 tsp mixed spice<br>
            4 tbsp icing sugar<br>
            100g/3½ oz pecans, chopped, divided into 8 piles<br>
            100g/3½ oz dates, chopped, divided into 8 piles<br>
            4 tbsp maple syrup<br>
            85g/3oz/5½ tbsp vegan margarine, melted</p>
          <p>1. If the filo pastry is frozen allow it to defrost thoroughly before starting.<br>
            2. Preheat the oven to 400ºF/200ºC/Gas Mark 6. <br>
            3. Stir the mixed spice into the icing sugar.<br>
            4. Carefully separate one sheet of filo pastry and lay flat on to a work surface. Brush with melted margarine.<br>
            5. Sprinkle ½ tsp of the sugar mixture over half of the pastry lengthways.<br>
            6. Sprinkle an eighth of the pecans of top of the sugar.<br>
            7. Fold the plain side of the pastry over the side with the filling on and press around the edges.<br>
            8. Place an eighth of the dates on top and drizzle with ½ tsp of maple syrup.<br>
            9. Fold the bottom left corner up to the opposite edge to form a triangle. Then keep folding the triangle over and over down the pastry until you reach the end of the pastry.<br>
            10. Repeat steps 4 to 9 until you have 8 parcels.<br>
            11. Place the parcels onto a greased baking tray and brush the top with melted margarine.<br>
            12. Cook on the top shelf of the oven for 10 minutes or until golden brown.</p>
          <p>Options: other combinations of fruit and nut which work well are cherry and walnut or almond and apricot.</p>
