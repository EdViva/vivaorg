          <h1>Recipes</h1>
          <h2><strong>Pears in Red Wine &amp; Cinnamon </strong></h2>
          <p>Serves 4</p>
          <p>3 hours plus 5-10 minutes preparation time and a few hours to chill</p>
          <p>This is a very easy dessert that can be gently baking while you do other things – if it’s&nbsp; made the day before, all you need to do is bring it out of the fridge and serve!<br>
  &nbsp;<br>
            8 large hard pears<br>
            560ml/1 pint red wine<br>
            50g/2oz caster sugar<br>
            2 whole cinnamon sticks<br>
            1tsp vanilla essence<br>
            1 rounded dsp arrowroot</p>
          <ol start="1" type="1">
            <li>Preheat oven to 130ºC/250ºF/Gas Mark ½.</li>
            <li>Peel pears leaving stalk intact. </li>
            <li>Place in a casserole dish with wine, sugar, cinnamon and      vanilla. </li>
            <li>Cover and bake for 1½ hours.</li>
            <li>Turn pears over and bake for a further 1½ hours. </li>
            <li>Remove pears and cinnamon and leave to cool.</li>
            <li>Mix arrowroot into a little cold water and over a medium heat      whisk into red wine mixture until thickened. </li>
            <li>Pour over pears and chill.</li>
          </ol>
