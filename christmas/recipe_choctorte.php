          <h2><strong>Chocolate &amp; Brandy Truffle Torte</strong></h2>
          <p>Serves 8<br><br>
            20 minutes plus overnight chilling time<br><br>
            Not only is this sophisticated little number a piece of chocolate heaven, it’s also incredibly quick and easy.<br><br>
            Cooking oil or low-cal spray<br>
            5tbsp liquid glucose (Supercook brand available from large supermarkets or else chemists and some  health food stores)<br>
            5tbs brandy<br>
            450g/1lb plain chocolate<br>
            250ml/½ pint tub soya cream (1 small tub)<br>
            75g/3oz regular Hob Nobs or similar oaty vegan biscuits <br>
            Sieved cocoa powder to decorate<br>
            Optional: raspberries or other berry fruit to garnish<br>
			</p>

