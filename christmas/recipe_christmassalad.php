          <h1>Recipes</h1>
          <h2><strong>Christmas Salad</strong></h2>
          <p>Serves 4</p>
          <p>10 minutes</p>
          <p>Tangy and refreshing, this delicious salad is a great source of Vitamin C during the cold winter months as well as being fast and easy to make. </p>
          <p>125g/4½ oz mixed unsalted nuts roughly chopped (brazils/hazelnuts/almonds/walnuts)<br>
            Mixed green salad leaves eg spinach, watercress and rocket<br>
            2 avocados<br>
            Lemon juice<br>
            4 kiwi fruit<br>
            8 fresh or tinned lychees<br>
            4 tbsp vegetable oil<br>
            1 tbsp cider vinegar or wine vinegar<br>
            Salt and pepper to taste<br>
            2 spring onions</p>
          <ol start="1" type="1">
            <li>Chop the green salad leaves and arrange on      a large platter or four individual plates.</li>
            <li>Peel and cube the avocados, sprinkling      them with lemon juice at the same time. Arrange on the bed of leaves.</li>
            <li>Peel and slice the kiwi fruits and arrange      with the avocado cubes.</li>
            <li>Peel the lychees (if fresh) and cut into      small pieces. Scatter them among the avocado and kiwi fruit.</li>
            <li>Mix the oil and vinegar together and      sprinkle over the salad. Season to taste.</li>
            <li>Scatter the chopped mixed nuts on top.</li>
            <li>Chop the spring onions finely and scatter      over the salad.</li>
          </ol>
