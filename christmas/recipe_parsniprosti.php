          <h1><img src="/christmas/food_images/parsnip.jpg" alt="" width="164" height="132" class="floatr">Recipes</h1>
          <h2><strong>Parsnip &amp; Potato Rosti with ‘Cream Cheese’ &amp; Cranberry </strong></h2>
          <p>            Serves 4</p>
          <p>25-30 minutes including preparation time. </p>
          <p>These traditional potato cakes get a bit of a gourmet make-over in this lovely recipe! <br>
              <br>
            2 large potatoes<br>
            3 large parsnips<br>
            Olive oil for frying<br>
            Salt and pepper<br>
            1 pack dairy-free ‘cream cheese’*<br>
            1 jar cranberry sauce<br>
            Chives to garnish </p>
          <ol start="1" type="1">
            <li>Pre-heat the oven to 180ºC/350ºF/Gas Mark 4. </li>
            <li>Parboil potatoes and parsnips whole for 10 minutes. Leave to      cool, then peel and grate. </li>
            <li>Mix with salt and pepper to taste and form into round patty      shapes, squeezing mixture together.</li>
            <li>Fry in olive oil until golden, turning once. Keep cooked rosti      in a warm oven while you fry the rest of the batch. </li>
            <li>When ready, top with a spoon of ‘cream cheese’ and a smaller      spoon of cranberry sauce. Decorate with chives.</li>
          </ol>
          <p>&nbsp;</p>
          *Such as Tofutti or Sheese brands - plain or flavoured with garlic/herbs - available in most health food shops.
