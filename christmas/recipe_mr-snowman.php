          <!-- InstanceBeginEditable name="content" --><h1>Recipes</h1>
         <a href="/christmas/images/large/snowman-vegetarian-christmas-cake.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-vegetarian-christmas-cake.jpg" alt="Mixing the ingredients together" align="left" style="margin:3px;" height="150px"></a><h2><strong>Snowy Snowmen</strong></h2>
          <p>These delightful additions to your festive table are fun and simple to make, using a filling of cupcakes and a cover of ready-to-roll marzipan.<br><br>Makes 4 snowmen<br><br>
            Preparation/cooking time 1 hour</p>
<a href="/christmas/images/large/snowman-vegetarian-vegan-ingredients.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-vegetarian-vegan-ingredients.jpg" alt="Cupcakes ingredients" align="right" style="margin:3px;" height="110px"></a><br><h3>Cupcakes</h3>
<p>126ml/ 8 fl oz soya milk<br>
1 tsp apple cider vinegar<br>
224g/8oz granulated sugar<br>
84ml/3 fl oz canola oil<br>
2 tsp vanilla extract<br>
110g/4 oz plain flour<br>
36g/1 oz dark cocoa powder<br>
¾ tsp bicarbonate of soda<br>
½ baking powder<br>
¼ salt <br></p>

<a href="/christmas/images/large/snowman-head-body.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-head-body.jpg" alt="Covering ingredients" align="right" style="margin:3px;" height="110px"></a>
<h3>Covering</h3> 
<p>3x500g packets marzipan<br>
2 varieties of natural food colouring<br>
1 packet of cake decorations for buttons and eyes<br>
</p>
<h3>Method</h3>
<p>

1.	Preheat oven to 180°C/350° F/Gas Mark 4 and line muffin pan with paper cases.<br><br>
2.	Whisk together soya milk and vinegar in a bowl and set aside.<br><br>
<a href="/christmas/images/large/snowman-whisk-liquids.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-whisk-liquids.jpg" alt="Whisk soya milk and vinegar in a bowl" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-vegetarian-vegan-whisk.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-vegetarian-vegan-whisk.jpg" alt="Add sugar, oil, vanilla extract to the soya milk and beat until foamy" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-cake-veggie.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-cake-veggie.jpg" alt="Add sugar, oil, vanilla extract to the soya milk and beat until foamy" align="absmiddle" style="margin:3px;" height="110px"></a>
<br>
3.	Add sugar, oil, vanilla extract to the soya milk and beat until foamy.<br><br>
4.	In separate bowl, sift together flour, cocoa, bicarbonate of soda and salt.<br><br>
<a href="/christmas/images/large/snowman-seive-flour-icing-sugar.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-seive-flour-icing-sugar.jpg" alt="Covering ingredients" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-measure-ingredients.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-measure-ingredients.jpg" alt="Covering ingredients" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-cake-christmas.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-cake-christmas.jpg" alt="Covering ingredients" align="absmiddle" style="margin:3px;" height="110px"></a>
<br>5.	Add in two batches of wet ingredients and beat until smooth.<br><br>
6.	Pour into cases filling ¾ of the way.<br><br>
7.	Bake for 20 mins then place on wire rack to cool. <br><br>

8.	Meanwhile roll out marzipan to around 3mm thick pieces, flouring surface and rolling pin with icing sugar.<br><br>
<a href="/christmas/images/large/snowman-marzipan-rolling-pin.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-marzipan-rolling-pin.jpg" alt="Meanwhile roll out marzipan to around 3mm thick pieces" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-veggie-body.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-veggie-body.jpg" alt="Stack two cakes on top of each other to make four snowmen bodies" align="absmiddle" style="margin:3px;" height="110px"></a>
<a href="/christmas/images/large/snowman-marzipan-vegetarian-vegan.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-marzipan-vegetarian-vegan.jpg" alt="Cut four smaller round shapes from the remaining four cakes to make the heads" align="absmiddle" style="margin:3px;" height="110px"></a>
<br>
9.	Cut out four rectangles of marzipan 10cm/4in x 20cm/8in and four circles (by cutting around circumference of mug). <br><br>
10.	Remove cupcakes from cases. Stack two cakes on top of each other to make four snowmen bodies. <br><br>
11.	Cut four smaller round shapes from the remaining four cakes to make the heads.<br><br>
<a href="/christmas/images/large/snowman-marzipan-roll-ball.jpg" rel="gb_imageset[Snowy Snowmen]"><img src="/christmas/images/med/med/snowman-marzipan-roll-ball.jpg" alt="Construct the snowmen" align="left" style="margin:3px;" height="110px"></a>12.	Construct the snowmen by wrapping one rectangle of marzipan around each body and sealing neatly by dabbing with water. Make heads by wrapping the marzipan circles around the small cake rounds. Adjust shape gently in your hands.<br><br>
13.	Secure the heads to the bodies by brushing the necks with water and pressing gently down. <br><br>
14.	Divide remaining marzipan in two and place in separate bowls for colouring.<br><br>
15.	Add 1 tbsp of each food colouring to each bowl, mix in with a spoon, then by hand, so colour runs throughout icing.<br><br>
16.	Decorate the snowmen with hats and scarves made from the coloured marzipan and eyes and buttons from cake decorations.<br><br>
17.	Sprinkle with icing sugar to finish. <br><br>




