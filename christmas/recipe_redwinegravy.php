          <h1>Recipes</h1>
          <h2><strong>Red Wine &amp; Porcini Mushoom Gravy</strong></h2>
          <p><strong>Serves 4</strong></p>
          <p><strong>25 minutes</strong></p>
          <p>½ packet (approximately 7-10g) porcini or mixed dried mushrooms<br>
            300ml/generous10 fl oz boiling water<br>
            2 tbsp medium-dark miso – soya bean paste, available from oriental or health food shops<br>
            2 tbsp cold water <br>
            4 shallots or one small red onion, finely chopped <br>
            1 tbsp olive oil <br>
            3 tbsp cornflour mixed with 4 tbsp cold water to a smooth paste <br>
            450ml16floz hot vegan stock <br>
            240ml/generous 8fl oz red wine <br>
            2 tbsp sherry – any flavour<br>
            ½ tsp basil<br>
            ½ tsp tarragon<br>
            1 large bay leaf<br>
            Salt and pepper to taste </p>
          <ol start="1" type="1">
            <li>Boil the      kettle. Place dried mushrooms in a jug or bowl, pour boiling water on them,      cover and set aside. </li>
            <li>In a large      saucepan on medium-high heat, sauté shallots/onions in oil until      translucent. </li>
            <li>Add the      stock then add cornflour paste and stir in well. </li>
            <li>Add wine,      sherry and herbs. Bring to boil, stirring thoroughly to ensure that no      lumps form.</li>
            <li>Lower heat      and simmer until sauce is thickened, stirring often. </li>
            <li>Meanwhile,      in a small bowl, mix miso with the cold water to a smooth paste. Set      aside.</li>
            <li>Add      porcini mushrooms and their soaking water to gravy– omitting gritty liquid      at bottom!</li>
            <li>If gravy      too thick, add a little more water/stock; if too thin, make a paste from 1      tsp cornflour and a splash of water – bring to boil again. Add more if      necessary. </li>
            <li>Blend      gravy to the texture you prefer – if using a goblet blender, return gravy      to pan. If using a stick blender you can whizz it directly in the gravy      pan. </li>
            <li>Stir in      miso paste but don’t allow gravy to boil.</li>
            <li>Taste,      adjust seasoning if necessary and serve. </li>
          </ol>
