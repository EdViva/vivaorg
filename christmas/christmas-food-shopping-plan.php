<h2>Vegan gravy</h2>
  How do you get that ‘meaty’ but kind rich taste?<br />
 
  <a href="http://veganrecipeclub.org.uk/search/node/gravy"><br>
  Home-made</a> vegan  gravy isn’t difficult. See <a href="http://veganrecipeclub.org.uk/search/node/gravy">our recipes here</a> <br />
  <strong><br>
  Ready-made options</strong>
  <ul>
  <li>Bisto Original Gravy powder (red tub), Bisto  Onion Gravy Granules (NOT Bisto complete Gravy Mix).</li>
  <li>Oxo Onion or Vegetable Gravy Granules are all  veggie/vegan</li>
  <li>Co-op Gravy Mix is another good one</li>
  <li>Marigold Vegetarian Organic Gravy Powder</li>
  <li>Naturally Good Vegetable Gravy Powder</li>
  <li>VBites Gourmet Gravy Powder (use extra for a  thicker gravy)</li>
</ul>
<p>Make gravy the day before if possible. Like curries and  stews, the flavours get a chance to party and will be all the better for it.  Plus one or two of the following will add flavour to home-made or ready-made  versions. <br />
  </p>
<p><strong>Gravy Flavours<br />
    </strong><br>
    <strong>Miso</strong> – medium or dark. A Japanese paste from large supermarkets or health  food shops. Look for brands such as Sanchi, Clearspring or Sainsbury’s. Rich  and strong, add a teaspoon at a time until you get the desired flavour<br />
    <br>
    <strong>Tamari or shoyu soya sauce</strong> – supermarkets or health food  shops. Brands include Clearspring, Essential, Sanchi or Kikoman. Not to be  confused with cheaper varieties, this the soya sauce equivalent of balsamic  vinegar! Rich, fermented and no additives, it adds depth of flavour to all  sorts of dishes<br />
    <br>
    <strong>Yeast extract</strong> – Marmite or supermarket own-brands are  cheaper but Meridian and Essential also sell excellent versions.<br />
    <br>
    <strong>Red wine, sherry or port</strong> – a splash of booze makes all the  difference. Even if you’re teetotal, alcohol is burned off in cooking, just  make sure the gravy comes to the boil then simmers for a while</p>
